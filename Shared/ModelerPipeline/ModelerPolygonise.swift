/// Based on http://paulbourke.net/geometry/polygonise/
class ModelerPolygonise {
    
    public typealias _Float16 = UInt16

    struct GRIDCELL {
        var p               : [float3] = [float3(), float3(), float3(), float3(), float3(), float3(), float3(), float3()]
        var val             : [Float] = [0, 0, 0, 0, 0, 0, 0, 0]
    }
    
    let model           : Model
    let kit             : ModelerKit
    
    var triangles       : [float3] = []
    
    var chunkSize       : Int = 0
    
    var pixelByteCount  : Int = 0
    var bytesPerRow     : Int = 0
    var bytesPerChunk   : Int = 0
    
    var startTime       : Double = 0
    var totalTime       : Double = 0
    var coresActive     : Int = 0
    
    var regionJobs      : [ModelerPolygonRegion] = []
    
    var semaphore       : DispatchSemaphore!
    var dispatchGroup   : DispatchGroup!
    
    var totalJobs       : Int = 0
    
    var jobsHandedOut   : Int = 0
    
    var objVertices     = ""
    var objFaces        = ""
    var objFaceCount    : Int = 1

    init(model: Model, kit: ModelerKit) {
        self.model = model
        self.kit = kit
        
        semaphore = DispatchSemaphore(value: 1)
        dispatchGroup = DispatchGroup()
    }
    
    /// Returns the next job
    func getNextJob() -> ModelerPolygonRegion?
    {
        semaphore.wait()
        var regionJob : ModelerPolygonRegion? = nil
        if regionJobs.isEmpty == false {
            regionJob = regionJobs.removeFirst()
            
            jobsHandedOut += 1
            print("Jobs handed out", jobsHandedOut)
        }
        semaphore.signal()
        return regionJob
    }
        
    /// Multithreaded polygonization
    func processTexture() {
        
        chunkSize = 50
        
        pixelByteCount = MemoryLayout<_Float16>.size
        bytesPerRow = chunkSize * pixelByteCount
        bytesPerChunk = bytesPerRow * chunkSize
                
        if let texture = kit.modelTexture {
        
            let chunks = texture.width / chunkSize

            //var texArray = Array<UInt16>(repeating: 0, count: chunkSize * chunkSize * chunkSize)

            for z in 0..<chunks {
                for y in 0..<chunks {
                    for x in 0..<chunks {

                        /*
                        texArray.withUnsafeMutableBytes { texArrayPtr in
                        
                            texture.getBytes(texArrayPtr.baseAddress!,
                                             bytesPerRow: bytesPerRow,
                                             bytesPerImage: bytesPerChunk,
                                             from: MTLRegionMake3D(x * chunkSize, y * chunkSize, z * chunkSize, chunkSize, chunkSize, chunkSize),
                                             mipmapLevel: 0,
                                             slice: 0)
                        }*/
                        
                        //let f = float16to32(&texArray, count: 50 * 50 * 50)
                        //processChunk(array: f, position: float3(Float(x), Float(y), Float(z)))
                        
                        let region = MTLRegionMake3D(x * chunkSize, y * chunkSize, z * chunkSize, chunkSize, chunkSize, chunkSize)
                        regionJobs.append(ModelerPolygonRegion(region: region, position: float3(Float(x), Float(y), Float(z))))
                    }
                }
            }
        }
                
        if regionJobs.isEmpty == false {
            
            let cores = ProcessInfo().activeProcessorCount

            startTime = Double(Date().timeIntervalSince1970)
            totalTime = 0
            coresActive = 0
            
            totalJobs = regionJobs.count
            
            func startThread() {
                coresActive += 1
                dispatchGroup.enter()
                DispatchQueue.global(qos: .utility).async {
                    //self.processChunk(array: <#T##[Float]#>, position: <#T##float3#>)()
                    self.processRegionJobs()
                }
            }

            for i in 0..<cores {
                if i < regionJobs.count {
                    startThread()
                }
            }
            print("Cores", cores, "Jobs", regionJobs.count, "Cores started:", coresActive)
        }
    }
    
    /// Thread interface
    func processRegionJobs()
    {
        var texArray = Array<UInt16>(repeating: 0, count: chunkSize * chunkSize * chunkSize)
        let chunkPolygoniser = ModelerPolygoniseChunk(chunkSize: chunkSize)
        
        while let job = getNextJob() {

            semaphore.wait()
            
            texArray.withUnsafeMutableBytes { texArrayPtr in
            
                if let texture = kit.modelTexture {

                    texture.getBytes(texArrayPtr.baseAddress!,
                                     bytesPerRow: bytesPerRow,
                                     bytesPerImage: bytesPerChunk,
                                     from: job.region,
                                     mipmapLevel: 0,
                                     slice: 0)
                }
            }
            
            semaphore.signal()

            // Polygonise
            let f = float16to32(&texArray, count: 50 * 50 * 50)
            chunkPolygoniser.processChunk(array: f, position: job.position)
            
            // Write the data to the obj buffers
            if chunkPolygoniser.triangles.isEmpty == false {
                self.objTriangleFeed(chunkPolygoniser.triangles)
                chunkPolygoniser.triangles = []
            }
        }
        
        semaphore.wait()
        coresActive -= 1
        triangles += chunkPolygoniser.triangles
        semaphore.signal()

        if coresActive == 0  {
            
            let myTime = Double(Date().timeIntervalSince1970) - startTime
            totalTime += myTime
            
            print(totalTime, triangles.count)
            
            //self.model.objData = toOBJ()
            self.model.objData = Data((objVertices + objFaces).utf8)
            
            DispatchQueue.main.async {
                self.model.polygonisationEnded.send()
            }
        }
    }
    
    func objTriangleFeed(_ triangles: [float3])
    {
        semaphore.wait()
        
        func writeVertex(_ v: float3) -> String {
            return String(format: "%.04f", v.x) + "    " +  String(format: "%.04f", v.y) + "    " +  String(format: "%.04f", v.z) + "\n"
        }
        
        var obj = ""
        
        for o in 0..<(triangles.count / 3) {
            
            let index = o * 3
            
            let v1 = triangles[index]
            let v2 = triangles[index+1]
            let v3 = triangles[index+2]
            
            obj += "v " + writeVertex(v1)
            obj += "v " + writeVertex(v2)
            obj += "v " + writeVertex(v3)
        }
        
        self.objVertices += obj
        
        obj = ""

        var index : Int = self.objFaceCount
        for _ in 0..<(triangles.count / 3) {
            obj += "f " + String(index) + "    " + String((index+1)) + "    "  + String(index+2) + "\n"
            index += 3
        }
        self.objFaceCount = index
        self.objFaces += obj

        semaphore.signal()
    }
    
    /// Convert the triangles to an MDLAsset
    func toMesh(device: MTLDevice) -> MDLAsset?
    {
        let allocator = MTKMeshBufferAllocator.init(device: device)
        let vertexBuffer = allocator.newBuffer(MemoryLayout<float3>.stride * triangles.count, type: .vertex)

        let vertexMap = vertexBuffer.map()
        vertexMap.bytes.assumingMemoryBound(to: float3.self).assign(from: triangles, count: triangles.count)

        let vertexDescriptor = MDLVertexDescriptor()
        vertexDescriptor.attributes[0] = MDLVertexAttribute(name: MDLVertexAttributePosition,
                                                            format: .float3,
                                                            offset: 0,
                                                            bufferIndex: 0)
        vertexDescriptor.layouts[0] = MDLVertexBufferLayout(stride: MemoryLayout<float3>.stride)


        let mdlMesh = MDLMesh(vertexBuffer: vertexBuffer,
                              vertexCount: triangles.count,
                              descriptor: vertexDescriptor,
                              submeshes: [])
        
        let asset = MDLAsset()
        asset.add(mdlMesh)
        return asset
    }
    
    /// Convert the triangles to single file OBJ
    func toOBJ() -> Data {
        var obj = ""
        
        func writeVertex(_ v: float3) -> String {
            return String(format: "%.04f", v.x) + "    " +  String(format: "%.04f", v.y) + "    " +  String(format: "%.04f", v.z) + "\n"
        }
        
        for o in 0..<(triangles.count / 3) {
            
            let index = o * 3
            
            let v1 = triangles[index]
            let v2 = triangles[index+1]
            let v3 = triangles[index+2]
            
            obj += "v " + writeVertex(v1)
            obj += "v " + writeVertex(v2)
            obj += "v " + writeVertex(v3)
        }
        
        var index : Int = 1
        for _ in 0..<(triangles.count / 3) {
            obj += "f " + String(index) + "    " + String((index+1)) + "    "  + String(index+2) + "\n"
            index += 3
        }
        
        return Data(obj.utf8)
    }
    
    // https://developer.apple.com/forums/thread/93282
    
    func float16to32(_ input: UnsafeMutablePointer<_Float16>, count: Int) -> [Float] {
        var output = [Float](repeating: 0, count: count)
        float16to32(input: input, output: &output, count: count)
        return output
    }
    
    func float16to32(input: UnsafeMutablePointer<_Float16>, output: UnsafeMutableRawPointer, count: Int) {
        var bufferFloat16 = vImage_Buffer(data: input, height: 1, width: UInt(count), rowBytes: count * 2)
        var bufferFloat32 = vImage_Buffer(data: output, height: 1, width: UInt(count), rowBytes: count * 4)

        if vImageConvert_Planar16FtoPlanarF(&bufferFloat16, &bufferFloat32, 0) != kvImageNoError {
            print("Error converting float16 to float32")
        }
    }
    

}

/// Polygonise chunks of data, each thread creates this worker class
class ModelerPolygoniseChunk {
    
    public typealias _Float16 = UInt16

    struct GRIDCELL {
        var p               : [float3] = [float3(), float3(), float3(), float3(), float3(), float3(), float3(), float3()]
        var val             : [Float] = [0, 0, 0, 0, 0, 0, 0, 0]
    }
    
    var triangles       : [float3] = []
    
    var chunkSize       : Int = 0
    
    init(chunkSize: Int) {
        self.chunkSize = chunkSize
    }
    
    /// Processes the given chunk of data, where position indicates the lower front left coordinate of the chunk in the 3D texture.
    func processChunk(array: [Float], position: float3)
    {
        let stepSize    : Float = 1.0 / Float(chunkSize)
        var p           = position
        
        let slice = chunkSize * chunkSize
        
        var gridCell = GRIDCELL()

        for z in 0..<(chunkSize-1) {

            p.y = position.y
            for y in 0..<(chunkSize-1) {
                
                p.x = position.x

                var frontOff = z * slice + y * chunkSize
                var backOff = (z + 1) * slice + y * chunkSize

                for _ in 0..<(chunkSize-1) {

                    gridCell.val[0] = array[backOff]
                    gridCell.val[1] = array[backOff + 1]
                    gridCell.val[4] = array[backOff + chunkSize]
                    gridCell.val[5] = array[backOff + chunkSize + 1]

                    gridCell.val[2] = array[frontOff + 1]
                    gridCell.val[3] = array[frontOff]
                    gridCell.val[6] = array[frontOff + chunkSize + 1]
                    gridCell.val[7] = array[frontOff + chunkSize]
                    
                    gridCell.p[0] = float3(p.x, p.y, p.z + stepSize)
                    gridCell.p[1] = float3(p.x + stepSize, p.y, p.z + stepSize)
                    gridCell.p[4] = float3(p.x, p.y + stepSize, p.z + stepSize)
                    gridCell.p[5] = float3(p.x + stepSize, p.y + stepSize, p.z + stepSize)

                    gridCell.p[2] = float3(p.x + stepSize, p.y, p.z)
                    gridCell.p[3] = float3(p.x, p.y, p.z)
                    gridCell.p[6] = float3(p.x + stepSize, p.y + stepSize, p.z)
                    gridCell.p[7] = float3(p.x, p.y + stepSize, p.z)

                    polygonise(grid: gridCell)
                    
                    p.x += stepSize
                    
                    frontOff += 1
                    backOff += 1
                }
                p.y += stepSize
            }
            p.z += stepSize
        }
        
        //gridCell.val[0] = array
    }
    
    func polygonise(grid: GRIDCELL, isolevel: Float = 0)
    {
        //var i,ntriang;
        var cubeindex = 0
        var vertlist = Array<SIMD3<Float>>(repeating: float3(0, 0, 0), count: 12)
        
        // Determine the index into the edge table which
        // tells us which vertices are inside of the surface
        if grid.val[0] < isolevel { cubeindex |= 1 }
        if grid.val[1] < isolevel { cubeindex |= 2 }
        if grid.val[2] < isolevel { cubeindex |= 4 }
        if grid.val[3] < isolevel { cubeindex |= 8 }
        if grid.val[4] < isolevel { cubeindex |= 16 }
        if grid.val[5] < isolevel { cubeindex |= 32 }
        if grid.val[6] < isolevel { cubeindex |= 64 }
        if grid.val[7] < isolevel { cubeindex |= 128 }
        
        // Cube is entirely in/out of the surface
        if edgeTable[cubeindex] == 0 {
           return
        }
        
        if (edgeTable[cubeindex] & 1) != 0 {
           vertlist[0] = VertexInterp(isolevel,grid.p[0],grid.p[1],grid.val[0],grid.val[1])
        }
        if (edgeTable[cubeindex] & 2) != 0 {
            vertlist[1] = VertexInterp(isolevel,grid.p[1],grid.p[2],grid.val[1],grid.val[2])
        }
        if (edgeTable[cubeindex] & 4) != 0 {
            vertlist[2] = VertexInterp(isolevel,grid.p[2],grid.p[3],grid.val[2],grid.val[3])
        }
        if (edgeTable[cubeindex] & 8) != 0 {
            vertlist[3] = VertexInterp(isolevel,grid.p[3],grid.p[0],grid.val[3],grid.val[0])
        }
        if (edgeTable[cubeindex] & 16) != 0 {
            vertlist[4] = VertexInterp(isolevel,grid.p[4],grid.p[5],grid.val[4],grid.val[5])
        }
        if (edgeTable[cubeindex] & 32) != 0 {
            vertlist[5] = VertexInterp(isolevel,grid.p[5],grid.p[6],grid.val[5],grid.val[6])
        }
        if (edgeTable[cubeindex] & 64) != 0 {
            vertlist[6] = VertexInterp(isolevel,grid.p[6],grid.p[7],grid.val[6],grid.val[7])
        }
        if (edgeTable[cubeindex] & 128) != 0 {
            vertlist[7] = VertexInterp(isolevel,grid.p[7],grid.p[4],grid.val[7],grid.val[4])
        }
        if (edgeTable[cubeindex] & 256) != 0 {
            vertlist[8] = VertexInterp(isolevel,grid.p[0],grid.p[4],grid.val[0],grid.val[4])
        }
        if (edgeTable[cubeindex] & 512) != 0 {
            vertlist[9] = VertexInterp(isolevel,grid.p[1],grid.p[5],grid.val[1],grid.val[5])
        }
        if (edgeTable[cubeindex] & 1024) != 0 {
            vertlist[10] = VertexInterp(isolevel,grid.p[2],grid.p[6],grid.val[2],grid.val[6])
        }
        if (edgeTable[cubeindex] & 2048) != 0 {
            vertlist[11] = VertexInterp(isolevel,grid.p[3],grid.p[7],grid.val[3],grid.val[7])
        }
        
        var i = 0
        
        while triTable[cubeindex][i] != -1 {
                        
            let x = vertlist[triTable[cubeindex][i  ]]
            let y = vertlist[triTable[cubeindex][i+1]]
            let z = vertlist[triTable[cubeindex][i+2]]
            
            triangles.append(x)
            triangles.append(y)
            triangles.append(z)

            i += 3
        }
        
        //print("triangles", triangles.count / 3)
    }
    
    /// Linearly interpolate the position where an isosurface cuts an edge between two vertices, each with their own scalar value
    func VertexInterp(_ isolevel: Float,_ p1: float3,_ p2: float3,_ valp1: Float,_ valp2: Float) -> float3
    {
        var p  = float3()

        if abs(isolevel-valp1) < 0.00001 { return p1 }
        if abs(isolevel-valp2) < 0.00001 { return p2 }
        if abs(valp1-valp2) < 0.00001 { return p1 }
        
        let mu = (isolevel - valp1) / (valp2 - valp1)
        p.x = p1.x + mu * (p2.x - p1.x)
        p.y = p1.y + mu * (p2.y - p1.y)
        p.z = p1.z + mu * (p2.z - p1.z)

       return p
    }
    
